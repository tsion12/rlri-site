/** All locale files must share this shape; values are plain strings. */
export type Messages = DeepStringify<typeof en>;

export type TranslationKey = DeepKeyOf<Messages>;

type DeepStringify<T> = T extends string
  ? string
  : T extends object
    ? { [K in keyof T]: DeepStringify<T[K]> }
    : never;

/** Dot-notation keys for `t("nav.home")`. */
type DeepKeyOf<T, P extends string = ""> = T extends string
  ? P extends ""
    ? never
    : P
  : {
      [K in keyof T]: K extends string
        ? DeepKeyOf<T[K], P extends "" ? K : `${P}.${K}`>
        : never;
    }[keyof T];
